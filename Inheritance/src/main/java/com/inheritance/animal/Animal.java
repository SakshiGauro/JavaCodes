package com.inheritance.animal;

public class Animal
{
   private String type;
   private boolean isPet;
   
   private String getType()
   {
       return type;
   }
   
   public void setType(String type)
   {
       this.type = type;
   }
   
   public boolean getIsPet()
   {
       return isPet;
   }
   
    public void setIsPet(boolean isPet)
    {
        this.isPet = isPet;
    }
   
    
}
