package com.inheritance.computer;

public class Computer
{
    private int screenSize;
    private int memory;
    
    public int getScreenSize()
    {
        return screenSize;
    }
    
    public void setScreenSize(int val)
    {
        this.screenSize = val;
    }
    
    public int getMemory()
    {
        return memory;
    }
    
    public void setMemory(int memory)
    {
        this.memory = memory;
    }
}
